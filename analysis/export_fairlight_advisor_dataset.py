def _read_raw_panel() -> pd.DataFrame:
    raw = pd.read_parquet(RAW_PANEL_PATH, columns=RAW_PANEL_COLUMNS)
    raw["tax_period_end"] = pd.to_datetime(raw["tax_period_end"], errors="coerce")
