def _optional_str(value: Any, fallback: str = "") -> str:
    if pd.isna(value):
        return fallback
    return str(value)


def _optional_float(value: Any, default: float = 0.0) -> float:
    if pd.isna(value):
        return default
    return float(value)


def _compute_investment_yield(row: Any) -> float:
    """Investment income / net assets (EOY) as a percentage. Returns 0 if non-positive assets."""
    net_assets = row.get("net_assets_eoy")
    inv_income = row.get("investment_income")
    if pd.isna(net_assets) or pd.isna(inv_income) or float(net_assets) <= 0:
        return 0.0
    return round(float(inv_income) / float(net_assets) * 100.0, 3)


def _build_consecutive_investment_years(raw: pd.DataFrame) -> pd.DataFrame:
    """
    For each EIN, count consecutive years (working backward from latest fiscal_year)
    where investment_income > 0. Returns a DataFrame keyed on (ein, fiscal_year)
    where fiscal_year is the latest filing year observed for that EIN.
    """
    panel = raw[["ein", "fiscal_year", "investment_income"]].copy()
    panel = panel.dropna(subset=["ein", "fiscal_year"])
    panel["fiscal_year"] = panel["fiscal_year"].astype(int)
    panel = panel.sort_values(["ein", "fiscal_year"])

    results = []
    for ein, grp in panel.groupby("ein", sort=False):
        rows = grp.sort_values("fiscal_year").to_dict("records")
        latest_year = rows[-1]["fiscal_year"]
        year_to_ii = {int(r["fiscal_year"]): r["investment_income"] for r in rows}
        count = 0
        year = latest_year
        while year in year_to_ii:
            val = year_to_ii[year]
            if pd.notna(val) and float(val) > 0:
                count += 1
                year -= 1
            else:
                break
        results.append({
            "ein": ein,
            "fiscal_year": latest_year,
            "consecutive_years_with_investment_income": count,
        })
    return pd.DataFrame(results)


def _compute_data_completeness_score(row: Any) -> float:
    """0-8 score based on non-null counts of five financial fields (data_completeness component)."""
    fields = [
        "investment_income",
        "unrestricted_net_assets",
        "net_assets_boy",
        "temp_restricted_net_assets",
        "perm_restricted_net_assets",
    ]
    non_null = sum(1 for f in fields if not pd.isna(row.get(f)))
    return round(non_null / len(fields) * 8.0, 3)


def _format_currency(value: Any) -> str:
    amount = _optional_float(value, 0.0)
    if abs(amount) < 1_000:
        return "Unavailable"
    return "${:,.0f}".format(amount)


def _format_percent(value: Any) -> str:
    return "{:.1f}%".format(_optional_float(value, 0.0) * 100.0)


def _format_delta(value: Any) -> str:
    numeric = _optional_float(value, 0.0)
    return "{:+.2f}".format(numeric)


def _safe_ratio(numerator: pd.Series, denominator: pd.Series) -> pd.Series:
    denominator = pd.to_numeric(denominator, errors="coerce")
    numerator = pd.to_numeric(numerator, errors="coerce")
    return numerator.where(denominator.abs() > 1e-9) / denominator.where(denominator.abs() > 1e-9)


def _build_history_support(
    raw: pd.DataFrame,
    target_eins: set[str],
    target_peer_keys: set[tuple[str, str]],
) -> tuple[dict[str, list[dict[str, Any]]], dict[tuple[str, str], list[dict[str, Any]]], dict[str, list[dict[str, Any]]]]:
    panel = raw[raw["ein"].astype(str).isin(target_eins)].copy()
    panel["total_revenue"] = pd.to_numeric(panel["total_revenue"], errors="coerce")
    panel["total_expenses"] = pd.to_numeric(panel["total_expenses"], errors="coerce")
    panel["net_assets_eoy"] = pd.to_numeric(panel["net_assets_eoy"], errors="coerce")
    panel["cash_non_interest_bearing"] = pd.to_numeric(panel["cash_non_interest_bearing"], errors="coerce")
    panel["savings_temporary_investments"] = pd.to_numeric(panel["savings_temporary_investments"], errors="coerce")
    panel["operating_margin_ratio"] = _safe_ratio(panel["total_revenue"] - panel["total_expenses"], panel["total_revenue"])
    panel["liquid_reserves"] = panel["cash_non_interest_bearing"].fillna(0) + panel["savings_temporary_investments"].fillna(0)

    history_lookup: dict[str, list[dict[str, Any]]] = {}
    composition_lookup: dict[str, list[dict[str, Any]]] = {}

    for ein, group in panel.groupby("ein", sort=False):
        ordered = group.sort_values("fiscal_year")
        history_lookup[str(ein)] = [
            {
                "fiscalYear": int(row.fiscal_year),
                "revenue": round(_optional_float(row.total_revenue), 2),
                "expenses": round(_optional_float(row.total_expenses), 2),
                "netAssets": round(_optional_float(row.net_assets_eoy), 2),
                "liquidReserves": round(_optional_float(row.liquid_reserves), 2),
                "operatingMargin": round(_optional_float(row.operating_margin_ratio) * 100.0, 1),
            }
            for row in ordered.itertuples()
        ]
        composition_lookup[str(ein)] = [
            {
                "fiscalYear": int(row.fiscal_year),
                "contributionsPct": round(_optional_float(row.pct_contributions) * 100.0, 1),
                "programPct": round(_optional_float(row.pct_program_revenue) * 100.0, 1),
                "investmentPct": round(_optional_float(row.pct_investment_income) * 100.0, 1),
                "otherPct": round(_optional_float(row.pct_other_revenue) * 100.0, 1),
            }
            for row in ordered.itertuples()
        ]

    peer_lookup: dict[tuple[str, str], list[dict[str, Any]]] = {}
    peer_keys_df = pd.DataFrame(sorted(target_peer_keys), columns=["state", "ntee_major_category"])
    peer_source = raw.merge(peer_keys_df, on=["state", "ntee_major_category"], how="inner").copy()
    peer_source["total_revenue"] = pd.to_numeric(peer_source["total_revenue"], errors="coerce")
    peer_source["total_expenses"] = pd.to_numeric(peer_source["total_expenses"], errors="coerce")
    peer_source["operating_margin_ratio"] = _safe_ratio(
        peer_source["total_revenue"] - peer_source["total_expenses"],
        peer_source["total_revenue"],
    )

    peer_history = (
        peer_source.dropna(subset=["state", "ntee_major_category", "fiscal_year", "operating_margin_ratio"])
        .groupby(["state", "ntee_major_category", "fiscal_year"], as_index=False)
        .agg(
            peerMarginQ25=("operating_margin_ratio", lambda values: round(float(values.quantile(0.25) * 100.0), 1)),
            peerMarginMedian=("operating_margin_ratio", lambda values: round(float(values.quantile(0.50) * 100.0), 1)),
            peerMarginQ75=("operating_margin_ratio", lambda values: round(float(values.quantile(0.75) * 100.0), 1)),
        )
    )

    for (state, ntee), group in peer_history.groupby(["state", "ntee_major_category"], sort=False):
        peer_lookup[(str(state), str(ntee))] = [
            {
                "fiscalYear": int(row.fiscal_year),
                "peerMarginQ25": round(_optional_float(row.peerMarginQ25), 1),
                "peerMarginMedian": round(_optional_float(row.peerMarginMedian), 1),
                "peerMarginQ75": round(_optional_float(row.peerMarginQ75), 1),
            }
            for row in group.sort_values("fiscal_year").itertuples()
        ]

    return history_lookup, peer_lookup, composition_lookup


def _humanize_action_reason(row: pd.Series) -> str:
    label = row["action_label"]
    constraint = CONSTRAINT_LABELS.get(_optional_str(row.get("recovery_analog_constraint"), ""), "portfolio pressure")
    if label == "Underinvested Asset Base":
        return "Strong peer-relative profile with room to back from strength."
    if label == "Revenue Concentration Risk":
        return "Operationally viable, but concentration risk still needs active mitigation."
    if label == "Needs Data Diligence":
        if _optional_str(row.get("urgency_severity"), "none") == "acute":
            return "Immediate diligence is warranted before capital is committed."
        return "This case needs deeper diligence before a capital recommendation is defensible."
    if constraint == "runway pressure":
        return "The core issue is liquidity runway, which makes near-term support design decisive."
    if constraint == "operating deficit pressure":
        return "The core issue is margin pressure, so any support should be paired with operating reset discipline."
    return "The core issue is concentrated revenue dependence, so support should be tied to diversification progress."


def _confidence_note(row: pd.Series) -> str:
    confidence = _optional_str(row.get("checkpoint1_confidence_tier"), "Medium")
    benchmark_status = _optional_str(row.get("benchmark_status"), "ok")
    if confidence == "High":
        return "High confidence: peer benchmarks resolved cleanly and the filing history is stable enough for direct comparison."
    if confidence == "Low":
        return "Low confidence: the filing history or peer benchmark support is thin, so advisor judgment should dominate."
    if benchmark_status == "insufficient_resilient_refs":
        return "Medium confidence: the case is informative, but resilient peer references are limited."
    return "Medium confidence: the signal is usable, but it should be read with context rather than as a standalone answer."


