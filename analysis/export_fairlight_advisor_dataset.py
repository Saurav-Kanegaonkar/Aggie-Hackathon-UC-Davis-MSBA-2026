def _read_stage_data(raw: pd.DataFrame) -> pd.DataFrame:
    stage3 = pd.read_parquet(STAGE3_PATH)
    stage4 = pd.read_parquet(STAGE4_PATH)

    # Normalize legacy bucket names in stage3 action_label & memo_text
    # (parquet artifacts predate the bucket rename)
    # Map BOTH v1 (Amplify/Stabilize/Deep Review/Diversify) and v2 (Optimize/
    # Strengthen/Review/Diversify) bucket names to v2.1 names so the exporter
    # is resilient to whichever version the upstream parquet was written with.
    _BUCKET_RENAME = {
        "Amplify": "Underinvested Asset Base",
        "Optimize": "Underinvested Asset Base",
        "Stabilize": "Weak Financial Foundation",
        "Strengthen": "Weak Financial Foundation",
        "Deep Review": "Needs Data Diligence",
        "Review": "Needs Data Diligence",
        "Diversify": "Revenue Concentration Risk",
    }
    if "action_label" in stage3.columns:
        stage3["action_label"] = stage3["action_label"].replace(_BUCKET_RENAME)
    if "memo_text" in stage3.columns:
        # Memo text was generated by the upstream pipeline with v1 bucket names
        # ("Amplify", "Stabilize", "Deep Review", "Diversify") embedded in prose.
        # Rewrite those to v2.1 names. Order matters: "Deep Review" before any
        # standalone "Review" to avoid partial matches.
        s = stage3["memo_text"].astype(str)
        s = s.str.replace("Deep Review", "Needs Data Diligence", regex=False)
        s = s.str.replace("deep_review_low_confidence", "needs_data_diligence_low_confidence", regex=False)
        s = s.str.replace(r"\bAmplify\b", "Underinvested Asset Base", regex=True)
        s = s.str.replace(r"\bStabilize\b", "Weak Financial Foundation", regex=True)
        s = s.str.replace(r"\bDiversify\b", "Revenue Concentration Risk", regex=True)
        stage3["memo_text"] = s

    merged = stage3.merge(stage4, on=["ein", "fiscal_year"], how="left", validate="one_to_one")
    filing_history = (
        raw.dropna(subset=["ein", "fiscal_year"])
        .drop_duplicates(["ein", "fiscal_year"])
        .groupby("ein", as_index=False)
        .agg(
            filing_years_observed=("fiscal_year", "nunique"),
            first_filing_year=("fiscal_year", "min"),
            latest_filing_year=("fiscal_year", "max"),
        )
    )

    panel_join_cols = [
        "ein",
        "fiscal_year",
        "org_name",
        "ntee_major_category",
        "total_revenue",
        "net_assets_eoy",
        "net_assets_boy",
        "investment_income",
        "unrestricted_net_assets",
        "temp_restricted_net_assets",
        "perm_restricted_net_assets",
    ]
    merged = merged.merge(
        raw[panel_join_cols],
        on=["ein", "fiscal_year"],
        how="left",
        validate="one_to_one",
    )
    merged = merged.merge(filing_history, on="ein", how="left", validate="many_to_one")

    inv_streak = _build_consecutive_investment_years(raw)
    merged = merged.merge(inv_streak, on=["ein", "fiscal_year"], how="left")
    merged["consecutive_years_with_investment_income"] = (
        merged["consecutive_years_with_investment_income"].fillna(0).astype(int)
    )

    merged = merged.sort_values(["ein", "fiscal_year"]).groupby("ein", as_index=False).tail(1).reset_index(drop=True)
    return merged


